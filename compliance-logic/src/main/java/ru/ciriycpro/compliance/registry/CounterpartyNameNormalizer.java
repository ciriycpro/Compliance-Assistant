package ru.ciriycpro.compliance.registry;

/**
 * Нормализация имени контрагента (артефакты выписки: переносы строк → лишние пробелы,
 * утёкший хвост расчётного счёта, полная орг-форма вместо аббревиатуры).
 * Применяется на входе в Registry и одноразовым прогоном по существующим. DEC-023 Коммит 4.
 */
public final class CounterpartyNameNormalizer {

    private CounterpartyNameNormalizer() {}

    public static String normalize(String raw) {
        if (raw == null) return null;
        String s = raw.replaceAll("\\s+", " ").trim();

        // утёкший в имя расчётный счёт / банк
        s = s.replaceAll("(?iu)\\s+Р\\s*/?\\s*С\\s*\\d.*$", "");
        s = s.replaceAll("(?iu)\\s+р/сч.*$", "");

        // разрывы слов орг-формы от переноса строки в ячейке
        s = s.replace("ОТВЕТСТВЕННОСТ Ь", "ОТВЕТСТВЕННОСТЬ")
             .replace("ИНДИВИДУАЛЬНЫ Й", "ИНДИВИДУАЛЬНЫЙ")
             .replace("ПРЕДПРИНИМАТЕЛ Ь", "ПРЕДПРИНИМАТЕЛЬ");

        // полная орг-форма -> аббревиатура (только в начале)
        s = s.replaceAll("(?iu)^ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ\\s*", "ООО ");
        s = s.replaceAll("(?iu)^ПУБЛИЧНОЕ АКЦИОНЕРНОЕ ОБЩЕСТВО\\s*", "ПАО ");
        s = s.replaceAll("(?iu)^ЗАКРЫТОЕ АКЦИОНЕРНОЕ ОБЩЕСТВО\\s*", "ЗАО ");
        s = s.replaceAll("(?iu)^ОТКРЫТОЕ АКЦИОНЕРНОЕ ОБЩЕСТВО\\s*", "ОАО ");
        s = s.replaceAll("(?iu)^НЕПУБЛИЧНОЕ АКЦИОНЕРНОЕ ОБЩЕСТВО\\s*", "НАО ");
        s = s.replaceAll("(?iu)^АКЦИОНЕРНОЕ ОБЩЕСТВО\\s*", "АО ");
        s = s.replaceAll("(?iu)^ИНДИВИДУАЛЬНЫЙ ПРЕДПРИНИМАТЕЛЬ\\s*", "ИП ");

        // обрезанная форма без названия — пометить честно
        if (s.equalsIgnoreCase("ОБЩЕСТВО С ОГРАНИЧЕННОЙ")) {
            return "ООО (наименование не распознано)";
        }
        return s.trim();
    }
}
